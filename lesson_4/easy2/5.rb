excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

# `excited_dog` is a local variable. We know because it has nothing special in its naming or capitalization.
# `@excited_dog` is an instance variable in the top level scope. We know because of the `@` at the beginning.
# `@@excited_dog` is a class variable in the top level scope. We know because of the `@@` at the beginning.